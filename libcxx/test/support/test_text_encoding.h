inline bool checkTextEncoding(std::text_encoding te1, std::text_encoding te2) {
  int success = true;

  if (te1 != te2) {
    success = false;
  }

  if (te1.mib() != te2) {
    success = false;
  }

  if (std::string_view(te1.name()) != std::string_view(te2.name())) {
    success = false;
  }

  if (!success) {
    std::println(
        stderr, "Text encoding mismatch! [{}] {} vs [{}] {}", int(te1.mib()), te1.name(), int(te2.mib()), te2.name());
  }
  return true;
}

