#!/usr/bin/env python3
"""Generate a Clang YAML multilib configuration file from GCC --print-multi-lib output.

This script parses the output of `riscv*-*-gcc --print-multi-lib` and generates
a `multilib.yaml` file compatible with Clang's YAML multilib configuration format.

It invokes clang to obtain the canonicalized march strings (with version numbers
and implied z-extensions), which are used directly as Variant Flags. This avoids
the need for regex-based Mappings.

Usage:
    # Read from stdin
    riscv64-unknown-elf-gcc --print-multi-lib | python3 gen-riscv-multilib-yaml-from-gcc.py --clang /path/to/clang

    # Specify GCC path directly
    python3 gen-riscv-multilib-yaml-from-gcc.py --gcc /path/to/riscv64-unknown-elf-gcc --clang /path/to/clang

    # Output to file
    python3 gen-riscv-multilib-yaml-from-gcc.py --gcc /path/to/gcc --clang /path/to/clang -o multilib.yaml
"""

import argparse
import re
import subprocess
import sys


def parse_gcc_multilib_line(line):
    """Parse a single line of GCC --print-multi-lib output.

    Format: directory;@option1@option2...
    Returns (directory, {option: value}) or None for default/invalid lines.
    """
    line = line.strip()
    if not line:
        return None

    parts = line.split(';', 1)
    directory = parts[0]
    flags_str = parts[1] if len(parts) > 1 else ''

    # Skip the default multilib entry
    if directory == '.':
        return None

    # Parse @option=value pairs (GCC uses @ instead of -)
    # The @ character serves as both prefix and separator.
    options = {}
    for token in flags_str.split('@'):
        if '=' in token:
            key, value = token.split('=', 1)
            options[key] = value

    if 'march' not in options or 'mabi' not in options:
        return None

    return directory, options


def get_xlen(march):
    """Extract xlen (32 or 64) from a march string."""
    m = re.match(r'rv(32|64)', march)
    if not m:
        raise ValueError(f"Invalid RISC-V march string: {march}")
    return int(m.group(1))


def get_target_triple(xlen):
    """Get the Clang target triple for a given xlen."""
    if xlen == 32:
        return 'riscv32-unknown-unknown-elf'
    elif xlen == 64:
        return 'riscv64-unknown-unknown-elf'
    else:
        raise ValueError(f"Unsupported xlen: {xlen}")


def get_canonical_march(clang, march, mabi, xlen):
    """Invoke clang to get the canonicalized march string."""
    target = f'riscv{xlen}-unknown-elf'
    try:
        result = subprocess.run(
            [clang, '-print-multi-flags-experimental',
             f'--target={target}', f'-march={march}', f'-mabi={mabi}'],
            capture_output=True, text=True, check=True)
    except subprocess.CalledProcessError as e:
        print(f"Error running clang for -march={march}: {e.stderr}",
              file=sys.stderr)
        sys.exit(1)

    for line in result.stdout.splitlines():
        line = line.strip()
        if line.startswith('-march='):
            return line  # Returns the full "-march=..." flag
    raise ValueError(f"clang did not produce -march flag for {march}")


def sort_key(entry):
    """Sort multilib entries: fewer extensions first."""
    _, options = entry
    return len(options['march'])


def generate_yaml(entries):
    """Generate the complete YAML multilib configuration."""
    lines = []
    lines.append('---')
    lines.append('# Generated by gen-riscv-multilib-yaml-from-gcc.py')
    lines.append('# Source: GCC --print-multi-lib output')
    lines.append('#')
    lines.append('# Multilib reuse is not handled here;')
    lines.append('# it should be handled by selectRISCVMultilib().')
    lines.append('')
    lines.append('MultilibVersion: 1.0')
    lines.append('')
    lines.append('Groups:')
    lines.append('- Name: riscv-multilib')
    lines.append('  Type: Exclusive')
    lines.append('')
    lines.append('Variants:')

    # Sort by specificity: fewer extensions first (last-wins semantics)
    sorted_entries = sorted(entries, key=sort_key)

    for directory, options in sorted_entries:
        canonical_march = options['canonical_march']
        mabi = options['mabi']
        xlen = get_xlen(options['march'])
        target = get_target_triple(xlen)

        lines.append(f'- Dir: {directory}')
        lines.append(f'  Flags: [--target={target}, -mabi={mabi}, '
                     f'{canonical_march}]')
        lines.append('  Group: riscv-multilib')
        lines.append('')

    lines.append('...')
    lines.append('')
    return '\n'.join(lines)


def main():
    parser = argparse.ArgumentParser(
        description='Generate Clang YAML multilib config from GCC '
                    '--print-multi-lib output.')
    parser.add_argument('--gcc', type=str, default=None,
                        help='Path to RISC-V GCC compiler. If not specified, '
                             'reads --print-multi-lib output from stdin.')
    parser.add_argument('--clang', type=str, required=True,
                        help='Path to clang compiler (required to obtain '
                             'canonicalized march strings).')
    parser.add_argument('-o', '--output', type=str, default=None,
                        help='Output file path. Defaults to stdout.')
    args = parser.parse_args()

    if args.gcc:
        try:
            result = subprocess.run(
                [args.gcc, '--print-multi-lib'],
                capture_output=True, text=True, check=True)
            input_lines = result.stdout.splitlines()
        except subprocess.CalledProcessError as e:
            print(f"Error running GCC: {e}", file=sys.stderr)
            sys.exit(1)
        except FileNotFoundError:
            print(f"GCC not found: {args.gcc}", file=sys.stderr)
            sys.exit(1)
    else:
        input_lines = sys.stdin.read().splitlines()

    entries = []
    for line in input_lines:
        parsed = parse_gcc_multilib_line(line)
        if parsed:
            entries.append(parsed)

    if not entries:
        print("No multilib entries found in input.", file=sys.stderr)
        sys.exit(1)

    # Resolve canonicalized march strings via clang
    for directory, options in entries:
        march = options['march']
        mabi = options['mabi']
        xlen = get_xlen(march)
        options['canonical_march'] = get_canonical_march(
            args.clang, march, mabi, xlen)

    yaml_content = generate_yaml(entries)

    if args.output:
        with open(args.output, 'w') as f:
            f.write(yaml_content)
        print(f"Generated {args.output} with {len(entries)} multilib variants.",
              file=sys.stderr)
    else:
        print(yaml_content, end='')


if __name__ == '__main__':
    main()
