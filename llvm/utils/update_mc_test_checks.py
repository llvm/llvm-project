#!/usr/bin/env python3
"""
A test update script.  This script is a utility to update LLVM 'llvm-mc' based test cases with new FileCheck patterns.
"""

from __future__ import annotations, print_function

from sys import stderr
from traceback import print_exc
import argparse
import collections
import functools
import os  # Used to advertise this file's name ("autogenerated_note").
import subprocess
import re
import sys

from UpdateTestChecks import common

mc_LIKE_TOOLS = [
    "llvm-mc",
]
ERROR_RE = re.compile(r":\d+: (warning|error): .*")
ERROR_CHECK_RE = re.compile(r"# COM: .*")
OUTPUT_SKIPPED_RE = re.compile(r"(\.text|^\s*[a-zA-Z0-9_]+\s*=)")
COMMENT = {"asm": "//", "dasm": "#"}

SUBSTITUTIONS = [
    ("%extract-encodings", "sed -n 's/.*encoding://p'"),
]


def argparse_callback(args):
    if args.llvm_objdump_binary is None and args.llvm_mc_binary:
        mc_dir = os.path.dirname(args.llvm_mc_binary)
        if mc_dir:
            args.llvm_objdump_binary = os.path.join(mc_dir, "llvm-objdump")


class Error(Exception):
    def __init__(self, test_info, line_no, msg):
        super().__init__(f"{test_info.path}:{line_no}: {msg}")


def invoke_tool(
    exe,
    check_rc,
    cmd_args,
    full_input,
    verbose=False,
    sourcepath=None,
    objdump_binary=None,
):
    substitutions = (
        common.getSubstitutions(sourcepath)
        + SUBSTITUTIONS
        + [(t, exe) for t in mc_LIKE_TOOLS]
    )
    if objdump_binary:
        substitutions.append(("llvm-objdump", objdump_binary))
    args = [
        common.applySubstitutions(cmd, substitutions) for cmd in cmd_args.split("|")
    ]
    is_objdump = any("llvm-objdump" in cmd for cmd in args)
    if is_objdump:
        new_args = []
        for idx, cmd in enumerate(args):
            c = cmd.strip()
            if idx == 0:
                if " -g " not in f" {c} ":
                    c += " -g"
            elif "llvm-objdump" in c:
                if " -l " not in f" {c} ":
                    c += " -l"
            new_args.append(c)
        args = new_args

    cmd = exe + " " + " | ".join(args)
    if verbose:
        print("Command: ", cmd)

    try:
        completed = subprocess.run(
            cmd,
            shell=True,
            input=full_input.encode(),
            check=check_rc,
            stdout=subprocess.PIPE,
            stderr=subprocess.PIPE,
        )
    except subprocess.CalledProcessError as e:
        sys.stderr.write(f"Error running command: {cmd}\n")
        sys.stderr.write(f"Exit code: {e.returncode}\n")
        if e.stderr:
            sys.stderr.write(f"Stderr:\n{e.stderr.decode(errors='replace')}\n")
        raise e

    # Fix line endings to unix CR style.
    return completed.stdout.decode().replace("\r\n", "\n")


def isRunLine(l):
    return common.RUN_LINE_RE.match(l)


def hasErr(err):
    return err and ERROR_RE.search(err) is not None


def getErrString(err):
    if not err:
        return ""

    # take the first match
    for line in err.splitlines():
        s = ERROR_RE.search(line)
        if s:
            return s.group(0)
    return ""


def getOutputString(out):
    if not out:
        return ""
    lines = []

    for line in out.splitlines():
        if OUTPUT_SKIPPED_RE.search(line):
            continue
        if line.strip("\t ") == "":
            continue
        lines.append(line.lstrip("\t "))
    return "\n".join(lines)


def should_add_line_to_output(input_line, prefix_set, mc_mode):
    # special check line
    if mc_mode == "dasm" and ERROR_CHECK_RE.search(input_line):
        return False
    elif input_line.strip() == COMMENT[mc_mode]:
        return True
    else:
        return common.should_add_line_to_output(
            input_line,
            prefix_set,
            comment_marker=COMMENT[mc_mode],
        )


def getStdCheckLine(
    prefix: str,
    line: str,
    mc_mode,
    use_same_for_encoding=False,
    is_next=False,
) -> str:
    if line.startswith("# <MCInst "):
        line += "{{$}}"
        maybe_next = "-NEXT" if is_next else ""
        return f"{COMMENT[mc_mode]} {prefix}{maybe_next}: {line}"
    elif use_same_for_encoding and "encoding:" in line:
        return f"{COMMENT[mc_mode]} {prefix}-SAME: {line}"
    else:
        maybe_next = "-NEXT" if is_next else ""
        return f"{COMMENT[mc_mode]} {prefix}{maybe_next}: {line}"


def select_prefixes_for_line(p_outputs, prefix_active_runs):
    def norm_ws(s):
        if s is None:
            return None
        return re.sub(r"\s+", " ", s).strip()

    p_dict = {}
    for p, run_id_outputs in p_outputs.items():
        run_ids = [run_id for run_id, o in run_id_outputs]
        outputs = [o for run_id, o in run_id_outputs]
        if all(norm_ws(o) == norm_ws(outputs[0]) for o in outputs):
            p_dict[p] = outputs[0], run_ids
        else:
            p_dict[p] = None, []

    used_run_ids = set()
    selected_prefixes = set()
    get_num_runs = lambda item: len(item[1][1])
    p_dict_sorted = sorted(p_dict.items(), key=get_num_runs, reverse=True)
    for prefix, (o, run_ids) in p_dict_sorted:
        if not run_ids:
            continue
        # The prefix MUST be active in exactly the same runs as run_ids!
        if set(run_ids) != prefix_active_runs[prefix]:
            continue
        # Check redundancy
        is_redundant = False
        for p_sel in selected_prefixes:
            o_sel, run_ids_sel = p_dict[p_sel]
            if norm_ws(o_sel) == norm_ws(o) and set(run_ids).issubset(set(run_ids_sel)):
                is_redundant = True
                break
        if is_redundant:
            continue

        if used_run_ids.isdisjoint(run_ids):
            selected_prefixes.add(prefix)
        used_run_ids.update(run_ids)
    return selected_prefixes, p_dict


def getStdCheckLines(
    prefix: str,
    output: str,
    mc_mode,
    use_same_for_encoding=False,
    skip_first_line=False,
) -> list[str]:
    o = []
    for i, line in enumerate(output.splitlines()):
        maybe_next = "-NEXT" if i > 0 else ""
        if i == 0 and skip_first_line:
            continue
        # Add an extra end-of-line check for --show-inst MCInst lines to
        # ensure we matched the full instruction name and not just a prefix.
        if line.startswith("# <MCInst "):
            line += "{{$}}"
            o.append(f"{COMMENT[mc_mode]} {prefix}{maybe_next}: {line}")
        elif use_same_for_encoding and "encoding:" in line:
            o.append(f"{COMMENT[mc_mode]} {prefix}-SAME: {line}")
        else:
            o.append(f"{COMMENT[mc_mode]} {prefix}{maybe_next}: {line}")
    return o


def getErrCheckLines(prefix: str, output: str, mc_mode, line_offset=1) -> list[str]:
    return [f"{COMMENT[mc_mode]} {prefix}: :[[@LINE-{line_offset}]]{output}"]


def parse_token_defs(test_info):
    tokens = {}
    current_token = None
    for line_no, line in enumerate(test_info.input_lines, start=1):
        # Remove comments.
        line = line.split("#")[0].rstrip()

        # Skip everything up to the instructions definition.
        if not tokens and not current_token and line != "//  INSTS=":
            continue

        if not line.startswith("//"):
            break

        original_len = len(line)
        line = line[2:].lstrip(" ")
        indent = original_len - len(line)

        if not line:
            current_token = None
            continue

        # Define a new token.
        if not current_token:
            if indent != 4 or not line.endswith("="):
                raise Error(test_info, line_no, "token definition expected")

            current_token = line[:-1].strip()
            if current_token in tokens:
                raise Error(test_info, line_no, f"'{current_token}' redefined")

            tokens[current_token] = []
            continue

        # Add token value.
        if indent != 8:
            raise Error(test_info, line_no, "wrong indentation for token value")

        tokens[current_token].append(line)

    return tokens


def expand_insts(tokens):
    def subst(s):
        for token, values in tokens.items():
            if token in s:
                for value in values:
                    yield from subst(s.replace(token, value, 1))
                return

        yield s

    yield from subst("INSTS")


def update_test(ti: common.TestInfo):
    if ti.path.endswith(".s"):
        mc_mode = "asm"
    elif ti.path.endswith(".txt"):
        mc_mode = "dasm"
    else:
        common.warn("Expected .s and .txt, Skipping file : ", ti.path)
        return

    triple_in_ir = None
    for l in ti.input_lines:
        m = common.TRIPLE_IR_RE.match(l)
        if m:
            triple_in_ir = m.groups()[0]
            break

    run_list = []
    for l in ti.run_lines:
        if "|" not in l:
            common.warn("Skipping unparsable RUN line: " + l)
            continue

        commands = [cmd.strip() for cmd in l.split("|")]
        assert len(commands) >= 2
        mc_cmd = " | ".join(commands[:-1])
        if (
            "-filetype=obj" in mc_cmd or re.search(r"-filetype\s+obj", mc_cmd)
        ) and "llvm-objdump" not in mc_cmd:
            common.warn("Skipping object-emitting RUN line: " + l)
            continue
        filecheck_cmd = commands[-1]

        # special handling for negating exit status
        # if not is used in runline, disable rc check, since
        # the command might or might not
        # return non-zero code on a single line run
        check_rc = True
        mc_cmd_args = mc_cmd.strip().split()
        if mc_cmd_args[0] == "not":
            check_rc = False
            mc_tool = mc_cmd_args[1]
            mc_cmd = mc_cmd[len(mc_cmd_args[0]) :].strip()
        else:
            mc_tool = mc_cmd_args[0]

        triple_in_cmd = None
        m = common.TRIPLE_ARG_RE.search(mc_cmd)
        if m:
            triple_in_cmd = m.groups()[0]

        march_in_cmd = ti.args.default_march
        m = common.MARCH_ARG_RE.search(mc_cmd)
        if m:
            march_in_cmd = m.groups()[0]

        common.verify_filecheck_prefixes(filecheck_cmd)

        mc_like_tools = mc_LIKE_TOOLS[:]
        if ti.args.tool:
            mc_like_tools.append(ti.args.tool)
        if mc_tool not in mc_like_tools:
            common.warn("Skipping non-mc RUN line: " + l)
            continue

        if not filecheck_cmd.startswith("FileCheck "):
            common.warn("Skipping non-FileChecked RUN line: " + l)
            continue

        mc_cmd_args = mc_cmd[len(mc_tool) :].strip()
        mc_cmd_args = mc_cmd_args.replace("< %s", "").replace("%s", "").strip()
        check_prefixes = common.get_check_prefixes(filecheck_cmd)

        run_list.append(
            (
                check_prefixes,
                mc_tool,
                check_rc,
                mc_cmd_args,
                triple_in_cmd,
                march_in_cmd,
            )
        )

    # Where instruction templates are specified, expand them in-place in ti.input_lines.
    if mc_mode == "asm":
        tokens = parse_token_defs(ti)
        if "INSTS" in tokens:
            testlines = list(expand_insts(tokens))
            # Keep only leading comments and empty lines (the template definitions)
            template_comments = []
            for line in ti.input_lines:
                if not line or line.startswith(COMMENT[mc_mode]):
                    template_comments.append(line)
                else:
                    break
            while template_comments and not template_comments[-1]:
                template_comments.pop()
            # Append the expanded instructions
            ti.input_lines = template_comments
            for inst in testlines:
                ti.input_lines.extend(["", inst])

    all_runs_line_outputs = []
    raw_prefixes = []
    for (
        prefixes,
        mc_tool,
        check_rc,
        mc_args,
        triple_in_cmd,
        march_in_cmd,
    ) in run_list:
        common.debug("Extracted mc cmd:", mc_tool, mc_args)
        common.debug("Extracted FileCheck prefixes:", str(prefixes))
        common.debug("Extracted triple :", str(triple_in_cmd))
        common.debug("Extracted march:", str(march_in_cmd))

        triple = triple_in_cmd or triple_in_ir
        if not triple:
            triple = common.get_triple_from_march(march_in_cmd)

        full_input = "\n".join(ti.input_lines)

        if "llvm-objdump" not in mc_args and "--show-source-loc" not in mc_args:
            mc_args += " --show-source-loc"

        full_out = invoke_tool(
            ti.args.llvm_mc_binary or mc_tool,
            check_rc,
            mc_args,
            full_input,
            verbose=ti.args.verbose,
            sourcepath=ti.path,
            objdump_binary=ti.args.llvm_objdump_binary,
        )
        line_outputs = collections.defaultdict(list)
        pending_lines = []
        ignore_count = 0
        discard_pending = False
        current_line_num = None
        warned_unknown = False
        for line in full_out.splitlines():
            line = line.strip()
            if not line:
                continue
            if OUTPUT_SKIPPED_RE.search(line):
                continue
            if ignore_count > 0:
                ignore_count -= 1
                continue
            m_loc = re.search(
                r"(?:#|//|;)\s*<(SourceLoc|MacroLoc|IncludeLoc):\s*([^:]+):(\d+):\d+>",
                line,
            )
            m_objdump_loc = re.search(
                r";\s*.*?[/<]stdin>:(\d+)",
                line,
            )
            if m_loc:
                loc_type = m_loc.group(1)
                filename = m_loc.group(2)
                line_num = int(m_loc.group(3))

                if filename != "<stdin>":
                    discard_pending = True

                if discard_pending:
                    pending_lines = []
                elif loc_type in ("SourceLoc", "IncludeLoc") and filename == "<stdin>":
                    line_outputs[line_num].extend(pending_lines)
                    pending_lines = []
                continue
            elif m_objdump_loc:
                line_num = int(m_objdump_loc.group(1))
                if current_line_num is not None and pending_lines:
                    line_outputs[current_line_num].extend(pending_lines)
                current_line_num = line_num
                pending_lines = []
                discard_pending = False
                continue

            m_err = re.match(r"<stdin>:(\d+):\d+: (?:warning|error):", line)
            if m_err:
                err_line_num = int(m_err.group(1))
                line_outputs[err_line_num].append(line)
                ignore_count = 2
                discard_pending = False
            else:
                if discard_pending:
                    discard_pending = False
                # If this is a disassembled instruction in llvm-objdump, clean it!
                m_inst = re.match(
                    r"^\s*[0-9a-f]+:\s*(?:(?:[0-9a-f]{2}\s+){1,8}|[0-9a-f]{8,16})\s+(.*)",
                    line,
                )
                if m_inst:
                    inst_str = m_inst.group(1).strip()
                    if "<unknown>" in inst_str and not warned_unknown:
                        common.warn(
                            "llvm-objdump returned '<unknown>' for instruction disassembly. "
                            "You may need to specify the correct path using --llvm-objdump-binary."
                        )
                        warned_unknown = True
                    # Normalize hex immediates 0x... to decimal to match llvm-mc output format
                    inst_str = re.sub(
                        r"\b0x([0-9a-fA-F]+)\b",
                        lambda m: str(int(m.group(1), 16)),
                        inst_str,
                    )
                    pending_lines.append(inst_str)
                elif "llvm-objdump" not in mc_args:
                    if ti.args.use_same_for_encoding:
                        m = re.search(r"(.*?)(\s*(?:#|//|;)\s*encoding:.*)", line)
                        if m:
                            instr = m.group(1).rstrip()
                            encoding = m.group(2).strip()
                            pending_lines.append(instr)
                            pending_lines.append(encoding)
                        else:
                            pending_lines.append(line)
                    else:
                        pending_lines.append(line)

        if current_line_num is not None and pending_lines:
            line_outputs[current_line_num].extend(pending_lines)

        all_runs_line_outputs.append(line_outputs)
        raw_prefixes.append(prefixes)

    # Collect the union of all line numbers across all runs
    all_line_nums = sorted(
        list(
            set().union(
                *(line_outputs.keys() for line_outputs in all_runs_line_outputs)
            )
        )
    )
    common.debug("Valid test line found: ", len(all_line_nums))

    # Reconstruct raw_output for each run based on all_line_nums
    raw_output = []
    for line_outputs in all_runs_line_outputs:
        run_outputs = []
        for line_num in all_line_nums:
            out_lines = line_outputs.get(line_num, [])
            run_outputs.append(out_lines)
        raw_output.append(run_outputs)
        common.debug("Collect raw tool lines:", str(len(raw_output[-1])))

    prefix_active_runs = collections.defaultdict(set)
    for run_id, prefixes in enumerate(raw_prefixes):
        for p in prefixes:
            prefix_active_runs[p].add(run_id)

    generated_prefixes = {}
    sort_keys = {}
    used_prefixes = set()
    prefix_set = set([prefix for p in run_list for prefix in p[0]])
    common.debug("Rewriting FileCheck prefixes:", str(prefix_set))
    ginfo = common.make_asm_generalizer(version=1)

    num_tests = len(all_line_nums)
    for test_id, line_num in enumerate(all_line_nums):
        input_line = line_num
        max_lines = max(
            len(raw_output[run_id][test_id]) for run_id in range(len(run_list))
        )

        # Sort key for disassembler tests
        if mc_mode == "dasm":
            instr_outs = []
            for run_id in range(len(run_list)):
                lines = raw_output[run_id][test_id]
                if lines:
                    out = lines[0]
                    if hasErr(out):
                        o = getErrString(out)
                    else:
                        o = getOutputString(out)
                    if "encoding:" in o:
                        instr_outs.append(o)
            sort_keys[input_line] = min(instr_outs) if instr_outs else input_line
            sort_keys[ti.input_lines[input_line - 1]] = sort_keys[input_line]

        check_lines = []
        prefix_seen_on_this_test_line = set()
        vars_seen = {prefix: dict() for p in run_list for prefix in p[0]}
        global_vars_seen = {prefix: dict() for p in run_list for prefix in p[0]}

        for line_idx in range(max_lines):
            p_outputs = collections.defaultdict(list)
            for run_id in range(len(run_list)):
                lines = raw_output[run_id][test_id]
                if line_idx < len(lines):
                    out = lines[line_idx]
                    if hasErr(out):
                        o = getErrString(out)
                    else:
                        o = getOutputString(out)
                    for p in raw_prefixes[run_id]:
                        p_outputs[p].append((run_id, o))

            selected_prefixes, p_dict = select_prefixes_for_line(
                p_outputs, prefix_active_runs
            )

            for prefix in sorted(selected_prefixes):
                o, run_ids = p_dict[prefix]
                used_prefixes.add(prefix)

                if hasErr(o):
                    line_offset = len(check_lines) + 1
                    checks = getErrCheckLines(prefix, o, mc_mode, line_offset)
                else:
                    is_next = prefix in prefix_seen_on_this_test_line
                    prefix_seen_on_this_test_line.add(prefix)
                    checks = [
                        getStdCheckLine(
                            prefix,
                            o,
                            mc_mode,
                            use_same_for_encoding=ti.args.use_same_for_encoding,
                            is_next=is_next,
                        )
                    ]

                    checks = common.generalize_check_lines(
                        checks, ginfo, vars_seen[prefix], global_vars_seen[prefix]
                    )

                check_lines.extend(checks)

        generated_prefixes[input_line] = "\n".join(check_lines)

    # write output
    output_lines = []
    for input_info in ti.iterlines(output_lines):
        input_line = input_info.line
        line_num = input_info.line_number + 1
        if line_num in all_line_nums:
            output_lines.append(input_line)
            if generated_prefixes.get(line_num):
                output_lines.append(generated_prefixes[line_num])

        elif should_add_line_to_output(input_line, prefix_set, mc_mode):
            output_lines.append(input_line)

    if ti.args.unique or ti.args.sort:
        # split with double newlines
        test_units = "\n".join(output_lines).split("\n\n")

        # select the key line for each test unit
        test_dic = {}
        for unit in test_units:
            lines = unit.split("\n")
            for l in lines:
                # if contains multiple lines, use
                # the first testline or runline as key
                l_strip = l.strip()
                if l_strip and not l_strip.startswith(ti.comment_prefix):
                    test_dic[unit] = l
                    break
                if isRunLine(l):
                    test_dic[unit] = l
                    break

        # unique
        if ti.args.unique:
            new_test_units = []
            written_lines = set()
            for unit in test_units:
                # if not testline/runline, we just add it
                if unit not in test_dic:
                    new_test_units.append(unit)
                else:
                    if test_dic[unit] in written_lines:
                        common.debug("Duplicated test skipped: ", unit)
                        continue

                    written_lines.add(test_dic[unit])
                    new_test_units.append(unit)
            test_units = new_test_units

        # sort
        if ti.args.sort:

            def getkey(l):
                # find key of test unit, otherwise use first line
                if l in test_dic:
                    line = test_dic[l]
                else:
                    line = l.split("\n")[0]

                # runline placed on the top
                return (not isRunLine(line), sort_keys.get(line, line))

            test_units = sorted(test_units, key=getkey)

        # join back to be output string
        output_lines = "\n\n".join(test_units).split("\n")

    # output
    if ti.args.gen_unused_prefix_body:
        output_lines.extend(ti.get_checks_for_unused_prefixes(run_list, used_prefixes))

    common.debug("Writing %d lines to %s..." % (len(output_lines), ti.path))
    with open(ti.path, "wb") as f:
        f.writelines(["{}\n".format(l).encode("utf-8") for l in output_lines])


def main():
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "--llvm-mc-binary",
        default=None,
        help='The "mc" binary to use to generate the test case',
    )
    parser.add_argument(
        "--llvm-objdump-binary",
        default=None,
        help='The "llvm-objdump" binary to use to generate the test case',
    )
    parser.add_argument(
        "--tool",
        default=None,
        help="Treat the given tool name as an mc-like tool for which check lines should be generated",
    )
    parser.add_argument(
        "--default-march",
        default=None,
        help="Set a default -march for when neither triple nor arch are found in a RUN line",
    )
    parser.add_argument(
        "--unique",
        action="store_true",
        default=False,
        help="remove duplicated test line if found",
    )
    parser.add_argument(
        "--sort",
        action="store_true",
        default=False,
        help="sort testline in alphabetic order (keep run-lines on top), this option could be dangerous as it"
        "could change the order of lines that are not expected",
    )
    parser.add_argument(
        "--use-same-for-encoding",
        action="store_true",
        default=False,
        help="Use CHECK-SAME to match the instruction encoding on a separate check line",
    )
    parser.add_argument("tests", nargs="+")
    initial_args = common.parse_commandline_args(parser)
    argparse_callback(initial_args)

    script_name = os.path.basename(__file__)

    returncode = 0
    for ti in common.itertests(
        initial_args.tests,
        parser,
        script_name="utils/" + script_name,
        argparse_callback=argparse_callback,
    ):
        try:
            update_test(ti)
        except Exception:
            stderr.write(f"Error: Failed to update test {ti.path}\n")
            print_exc()
            returncode = 1
    return returncode


if __name__ == "__main__":
    sys.exit(main())
