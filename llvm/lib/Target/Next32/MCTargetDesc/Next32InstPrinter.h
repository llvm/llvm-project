#ifndef LLVM_LIB_TARGET_Next32_INSTPRINTER_Next32INSTPRINTER_H
#define LLVM_LIB_TARGET_Next32_INSTPRINTER_Next32INSTPRINTER_H

#include "llvm/MC/MCExpr.h"
#include "llvm/MC/MCInstPrinter.h"

namespace llvm {
class Next32InstPrinter : public MCInstPrinter {
public:
  Next32InstPrinter(const MCAsmInfo &MAI, const MCInstrInfo &MII,
                    const MCRegisterInfo &MRI)
      : MCInstPrinter(MAI, MII, MRI) {}

  void printInst(const MCInst *MI, uint64_t Address, StringRef Annot,
                 const MCSubtargetInfo &STI, raw_ostream &OS) override;
  void printExpr(const MCExpr *MI, raw_ostream &O);
  void printOperand(const MCInst *MI, unsigned OpNo, raw_ostream &O,
                    const char *Modifier = nullptr);
  void printCondition(const MCInst *MI, unsigned OpNo, raw_ostream &O);
  void printInstructionSize(const MCInst *MI, unsigned OpNo, raw_ostream &O);
  void printMemoryAddressSpace(const MCInst *MI, unsigned OpNo, raw_ostream &O);
  void printVectorElementCount(const MCInst *MI, unsigned OpNo, raw_ostream &O);
  void printAlignSize(const MCInst *MI, unsigned OpNo, raw_ostream &O);
  void printRRIAttribute(const MCInst *MI, unsigned OpNo, raw_ostream &O);

  // Autogenerated by tblgen.
  std::pair<const char *, uint64_t> getMnemonic(const MCInst *MI) override;
  void printInstruction(const MCInst *MI, uint64_t Address, raw_ostream &O);
  static const char *getRegisterName(MCRegister RegNo);
};
} // namespace llvm

#endif
