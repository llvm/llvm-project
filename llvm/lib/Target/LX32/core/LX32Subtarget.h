// This file declares LX32Subtarget, the class that holds per-function target
// configuration and owns instances of the backend helper components.
//
// It is organized into the following sections:
//
//   Section 0 — Role in the backend pipeline
//   Section 1 — TableGen-generated base class import
//   Section 2 — Class declaration and constructor
//   Section 3 — Component accessors
//
//===----------------------------------------------------------------------===//
//
// Section 0 — Role in the backend pipeline
//
// LX32Subtarget serves two purposes:
//
//   1. Feature container.
//      Holds the set of enabled ISA features (e.g., whether the M-extension
//      multiply instructions are available).  For LX32 v1 there are no
//      optional features, but the infrastructure is in place.  Features are
//      parsed from -mcpu / -mattr flags by ParseSubtargetFeatures, whose body
//      is generated by TableGen from LX32Processors.td.
//
//   2. Component owner.
//      Owns by-value instances of the four main backend helper classes:
//        LX32RegisterInfo   — register descriptor + frame-index elimination
//        LX32InstrInfo      — instruction helpers + pseudo expansion
//        LX32FrameLowering  — prologue / epilogue generation
//        LX32TargetLowering — ISD operation legalisation (Day 9)
//      Owning them by value (not pointer) avoids a heap allocation per
//      subtarget and ties each component's lifetime to the subtarget.
//
// The subtarget is created by LX32TargetMachine::getSubtargetImpl and cached
// in a StringMap keyed by (CPU, FeatureString).  Different functions in the
// same module can have different subtargets via target-cpu / target-features
// function attributes.
//
//===----------------------------------------------------------------------===//

#ifndef LLVM_LIB_TARGET_LX32_CORE_LX32SUBTARGET_H
#define LLVM_LIB_TARGET_LX32_CORE_LX32SUBTARGET_H

#include "LX32FrameLowering.h"
#include "LX32ISelLowering.h"
#include "LX32InstrInfo.h"
#include "LX32RegisterInfo.h"
#include "llvm/ADT/StringRef.h"

//===----------------------------------------------------------------------===//
// Section 1 — TableGen-generated base class import
//
// GET_SUBTARGETINFO_HEADER emits the declaration of LX32GenSubtargetInfo,
// which inherits from TargetSubtargetInfo and provides:
//   - The ParseSubtargetFeatures declaration (body emitted in the .cpp).
//   - resolveSchedClass / resolveVariantSchedClass for scheduling.
//   - DFAPacketizer support (unused in LX32 v1).
//
// This include must appear before any component headers because they may
// reference types declared in the generated class.
//===----------------------------------------------------------------------===//

#define GET_SUBTARGETINFO_HEADER
#include "../TableGen/LX32GenSubtargetInfo.inc"

namespace llvm {

class Triple;
class LX32TargetMachine;

//===----------------------------------------------------------------------===//
// Section 2 — Class declaration and constructor
//===----------------------------------------------------------------------===//

class LX32Subtarget : public LX32GenSubtargetInfo {
  LX32RegisterInfo RegInfo;
  LX32InstrInfo InstrInfo;
  LX32FrameLowering FrameLowering;
  LX32TargetLowering TLInfo;

public:
  // Construct a subtarget for the given triple / CPU / feature combination.
  //
  //   TT      — target triple (used by the base class for OS/arch queries)
  //   CPU     — CPU name from -mcpu (e.g., "generic-lx32")
  //   TuneCPU — tuning target from -mtune (may differ from CPU for scheduling)
  //   FS      — feature string from -mattr (e.g., "+m" to enable multiply)
  //   TM      — the owning TargetMachine (needed by LX32TargetLowering)
  //
  // The constructor normalises CPU to "generic-lx32" when the caller passes
  // an empty string, preventing ParseSubtargetFeatures from warning about
  // an unrecognised processor.
  LX32Subtarget(const Triple &TT, StringRef CPU, StringRef TuneCPU,
                StringRef FS, const LX32TargetMachine &TM);

  // ParseSubtargetFeatures — parse -mcpu / -mattr flags into feature bits.
  //
  // The declaration is required by C++ even though the body is generated by
  // TableGen (emitted when GET_SUBTARGETINFO_TARGET_DESC is active in the
  // .cpp file).  Do not implement this manually — the generated body is the
  // source of truth.
  //
  // For LX32 v1 (no optional features) this is effectively a no-op beyond
  // printing debug output.  When optional extensions are added, the generated
  // body will set the corresponding feature-bit fields automatically.
  void ParseSubtargetFeatures(StringRef CPU, StringRef TuneCPU, StringRef FS);

  //===--------------------------------------------------------------------===//
  // Section 3 — Component accessors
  //
  // Override the pure-virtual hooks in TargetSubtargetInfo.  Each returns
  // a pointer to the corresponding by-value member once that component has
  // been implemented; nullptr is returned until then.
  //
  // Build-day status (update this comment as each day completes):
  //   Day 4  (this file)  — all return nullptr; skeleton compiles cleanly.
  //   Day 6  (RegisterInfo) — getRegisterInfo() returns &RegInfo.
  //   Day 7  (InstrInfo)    — getInstrInfo() returns &InstrInfo.
  //   Day 8  (FrameLowering) — getFrameLowering() returns &FrameLowering.
  //   Day 9  (ISelLowering)  — getTargetLowering() returns &TLInfo.
  //===--------------------------------------------------------------------===//

  const TargetRegisterInfo *getRegisterInfo() const override { return &RegInfo; }
  const TargetInstrInfo *getInstrInfo() const override { return &InstrInfo; }
  const TargetFrameLowering *getFrameLowering() const override {
    return &FrameLowering;
  }
  const TargetLowering *getTargetLowering() const override { return &TLInfo; }
};

} // namespace llvm

#endif // LLVM_LIB_TARGET_LX32_CORE_LX32SUBTARGET_H
