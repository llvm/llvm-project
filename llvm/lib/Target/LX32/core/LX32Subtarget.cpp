#include "LX32Subtarget.h"
#include "LX32TargetMachine.h"

#include "llvm/Support/Debug.h"

#define DEBUG_TYPE "lx32-subtarget"

//===----------------------------------------------------------------------===//
// Section 0 — TableGen-generated constructor and feature parser
//
// Two macros expand to the concrete bodies generated from LX32Processors.td
// and LX32.td.  They must appear at file scope (not inside a namespace)
// because the generated code opens its own `namespace llvm { ... }` blocks.
//
// GET_SUBTARGETINFO_CTOR
//   Emits the body of LX32GenSubtargetInfo::LX32GenSubtargetInfo(...).
//   This constructor passes the real scheduling tables (LX32WriteProcResTable,
//   LX32WriteLatencyTable, LX32ReadAdvanceTable) to TargetSubtargetInfo
//   instead of empty arrays.  Without this macro, the linker reports:
//     undefined symbol: LX32GenSubtargetInfo::LX32GenSubtargetInfo(...)
//   It also emits LX32GenSubtargetInfo::resolveSchedClass and
//   resolveVariantSchedClass, which are required by the virtual dispatch
//   table of TargetSubtargetInfo.
//
// GET_SUBTARGETINFO_TARGET_DESC
//   Emits the body of LX32Subtarget::ParseSubtargetFeatures(...).
//   For LX32 v1 (no optional features) the generated body only prints debug
//   output — but the symbol must be present or the linker will fail:
//     undefined symbol: llvm::LX32Subtarget::ParseSubtargetFeatures(...)
//   When optional extensions are added to LX32Processors.td, the generated
//   body will also set the corresponding boolean feature-bit fields declared
//   in LX32Subtarget (e.g., HasMExtension).
//===----------------------------------------------------------------------===//

#define GET_SUBTARGETINFO_CTOR
#define GET_SUBTARGETINFO_TARGET_DESC
#include "../TableGen/LX32GenSubtargetInfo.inc"

namespace llvm {

//===----------------------------------------------------------------------===//
// Section 1 — LX32Subtarget constructor
//===----------------------------------------------------------------------===//

LX32Subtarget::LX32Subtarget(const Triple &TT, StringRef CPU,
                              StringRef TuneCPU, StringRef FS,
                              const LX32TargetMachine &TM)
    // Delegate to the TableGen-generated base constructor.
    //
    // LX32GenSubtargetInfo(TT, CPU, TuneCPU, FS) passes the real scheduling
    // tables to TargetSubtargetInfo.  This is important: the previous skeleton
    // version constructed TargetSubtargetInfo directly with empty arrays, which
    // caused the processor-name lookup to fail with:
    //   'generic' is not a recognized processor for this target (ignoring)
    // Using the generated constructor fixes that because it passes the real
    // LX32SubTypeKV array (populated from LX32Processors.td) as the proc-desc.
    : LX32GenSubtargetInfo(TT, CPU, TuneCPU, FS),
      RegInfo(getHwMode()),
      InstrInfo(*this),
      FrameLowering(*this),
      TLInfo(TM, *this)
{

  // Normalise the CPU name before calling ParseSubtargetFeatures.
  //
  // LX32TargetMachine::getSubtargetImpl already normalises the CPU string
  // before constructing the subtarget, but we apply the same normalisation
  // here as a defensive measure — the subtarget constructor can be called
  // directly from tests or the MC layer where the normalisation may not have
  // happened yet.
  StringRef NormCPU = CPU.empty() ? "generic" : CPU;
  StringRef NormTune = TuneCPU.empty() ? NormCPU : TuneCPU;

  // ParseSubtargetFeatures is generated by GET_SUBTARGETINFO_TARGET_DESC above.
  // It processes the feature string FS and sets feature-bit fields in *this.
  // For LX32 v1, it is essentially a no-op (debug output only), but the call
  // must be present so that feature plumbing works correctly when optional
  // extensions are added.
  ParseSubtargetFeatures(NormCPU, NormTune, FS);
}

} // namespace llvm
