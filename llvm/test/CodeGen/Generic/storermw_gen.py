#!/usr/bin/env python3
"""Generate llvm/test/CodeGen/Generic/storermw.ll.

Emits IR only (no CHECK lines). After running this script, regenerate the
FileCheck assertions with:
  llvm/utils/update_test_checks.py --opt-binary <path-to-opt> \\
      llvm/test/CodeGen/Generic/storermw.ll

Coverage:
  - Every LangRef op (22 total) crossed with every valid ordering
    (monotonic, release, seq_cst) on scalar i32/float.
  - <2 x i32> elementwise for the 14 integer ops, all 4 orderings.
  - <2 x float> whole-vector and elementwise for the 8 FP ops, all 4 orderings.
  - Alternate integer sizes (i8, i16, i64) and FP type (double) for `add`/`fadd`.
  - Address spaces 0-3 for `add` on i32, all 4 orderings.
  - Volatile + syncscope variants for `add` i32.
"""

ORDERINGS = ["monotonic", "release", "seq_cst"]

INT_OPS = [
    "add",
    "sub",
    "and",
    "nand",
    "or",
    "xor",
    "max",
    "min",
    "umax",
    "umin",
    "uinc_wrap",
    "udec_wrap",
    "usub_cond",
    "usub_sat",
]
FP_OPS = [
    "fadd",
    "fsub",
    "fmax",
    "fmin",
    "fmaximum",
    "fminimum",
    "fmaximumnum",
    "fminimumnum",
]


def fn(
    name,
    ptr_ty,
    val_ty,
    op,
    ordering,
    align,
    elementwise=False,
    volatile=False,
    syncscope=None,
):
    ew = "elementwise " if elementwise else ""
    vol = "volatile " if volatile else ""
    ss = f'syncscope("{syncscope}") ' if syncscope else ""
    return (
        f"define void @{name}({ptr_ty} %addr, {val_ty} %val) {{\n"
        f"  storermw {vol}{ew}{op} {ptr_ty} %addr, {val_ty} %val "
        f"{ss}{ordering}, align {align}\n"
        f"  ret void\n"
        f"}}\n\n"
    )


def ord_suffix(ordering):
    return ordering.replace("_", "")  # seq_cst -> seqcst


def gen():
    out = []
    out.append(
        "; NOTE: Assertions have been autogenerated by utils/update_test_checks.py UTC_ARGS: --version 6\n"
    )
    out.append(
        "; RUN: opt < %s -passes='require<libcall-lowering-info>,atomic-expand' "
        "-mtriple=x86_64-unknown-linux-gnu -S | FileCheck %s\n"
    )
    out.append("; Test AtomicExpandPass handling of storermw:\n")
    out.append(";   monotonic/release/seq_cst -> atomicrmw with dead result\n")
    out.append(
        "; Generated by storermw_gen.py -- do not edit function bodies by hand;\n"
    )
    out.append("; edit the generator and rerun it, then update-opt-checks.\n\n")

    for ordering in ORDERINGS:
        suf = ord_suffix(ordering)
        out.append(f"; ===== ordering: {ordering} =====\n\n")

        out.append(f"; All LangRef integer ops on i32, {ordering}\n")
        for op in INT_OPS:
            out.append(fn(f"test_{op}_i32_{suf}", "ptr", "i32", op, ordering, 4))

        out.append(f"; All LangRef FP ops on float, {ordering}\n")
        for op in FP_OPS:
            out.append(fn(f"test_{op}_f32_{suf}", "ptr", "float", op, ordering, 4))

        out.append(f"; <2 x i32> elementwise (all integer ops), {ordering}\n")
        for op in INT_OPS:
            out.append(
                fn(
                    f"test_{op}_v2i32_elementwise_{suf}",
                    "ptr",
                    "<2 x i32>",
                    op,
                    ordering,
                    8,
                    elementwise=True,
                )
            )

        out.append(f"; <2 x float> whole-vector (all FP ops), {ordering}\n")
        for op in FP_OPS:
            out.append(
                fn(
                    f"test_{op}_v2f32_whole_{suf}",
                    "ptr",
                    "<2 x float>",
                    op,
                    ordering,
                    8,
                    elementwise=False,
                )
            )

        out.append(f"; <2 x float> elementwise (all FP ops), {ordering}\n")
        for op in FP_OPS:
            out.append(
                fn(
                    f"test_{op}_v2f32_elementwise_{suf}",
                    "ptr",
                    "<2 x float>",
                    op,
                    ordering,
                    8,
                    elementwise=True,
                )
            )

        out.append(f"; Alternate integer sizes for add, {ordering}\n")
        for ty, align in [("i8", 1), ("i16", 2), ("i64", 8)]:
            out.append(fn(f"test_add_{ty}_{suf}", "ptr", ty, "add", ordering, align))

        out.append(f"; Double for fadd, {ordering}\n")
        out.append(fn(f"test_fadd_f64_{suf}", "ptr", "double", "fadd", ordering, 8))

        out.append(f"; Address spaces (add i32), {ordering}\n")
        for asn in [0, 1, 2, 3]:
            out.append(
                fn(
                    f"test_add_i32_as{asn}_{suf}",
                    f"ptr addrspace({asn})",
                    "i32",
                    "add",
                    ordering,
                    4,
                )
            )

        out.append(f"; Volatile add i32, {ordering}\n")
        out.append(
            fn(
                f"test_volatile_add_i32_{suf}",
                "ptr",
                "i32",
                "add",
                ordering,
                4,
                volatile=True,
            )
        )

        out.append(f"; Syncscope singlethread add i32, {ordering}\n")
        out.append(
            fn(
                f"test_add_i32_syncscope_singlethread_{suf}",
                "ptr",
                "i32",
                "add",
                ordering,
                4,
                syncscope="singlethread",
            )
        )

    return "".join(out)


def main():
    import os

    here = os.path.dirname(os.path.abspath(__file__))
    path = os.path.join(here, "storermw.ll")
    with open(path, "w") as f:
        f.write(gen())
    print(f"wrote {path} (run update-opt-checks next)")


if __name__ == "__main__":
    main()
