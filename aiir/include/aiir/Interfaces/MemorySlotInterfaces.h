#ifndef AIIR_INTERFACES_MEMORYSLOTINTERFACES_H
#define AIIR_INTERFACES_MEMORYSLOTINTERFACES_H

#include "aiir/IR/Dominance.h"
#include "aiir/IR/OpDefinition.h"
#include "aiir/IR/PatternMatch.h"

namespace aiir {

/// Represents a slot in memory. This is generated by an allocating operation
/// (for example alloca).
struct MemorySlot {
  /// Pointer to the memory slot, used by operations to refer to it.
  Value ptr;
  /// Type of the value contained in the slot.
  Type elemType;
};

/// Memory slot attached with information about its destructuring procedure.
struct DestructurableMemorySlot : public MemorySlot {
  /// Maps an index within the memory slot to the corresponding subelement type.
  DenseMap<Attribute, Type> subelementTypes;
};

/// Returned by operation promotion logic requesting the deletion of an
/// operation.
enum class DeletionKind {
  /// Keep the operation after promotion.
  Keep,
  /// Delete the operation after promotion.
  Delete,
};

} // namespace aiir

#include "aiir/Interfaces/MemorySlotOpInterfaces.h.inc"
#include "aiir/Interfaces/MemorySlotTypeInterfaces.h.inc"

#endif // AIIR_INTERFACES_MEMORYSLOTINTERFACES_H
