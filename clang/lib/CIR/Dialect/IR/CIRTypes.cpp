Type CIRDialect::parseType(DialectAsmParser &parser) const {
  llvm::SMLoc typeLoc = parser.getCurrentLocation();
  StringRef mnemonic;
  Type genType;
  OptionalParseResult parseResult =
      generatedTypeParser(parser, &mnemonic, genType);
  if (parseResult.has_value())
    return genType;
  parser.emitError(typeLoc, "unknown type in CIR dialect");
  return Type();
}

void CIRDialect::printType(Type type, DialectAsmPrinter &os) const {
  if (failed(generatedTypePrinter(type, os)))
    llvm_unreachable("unexpected CIR type kind");
}

Type PointerType::parse(mlir::AsmParser &parser) {
  if (parser.parseLess())
    return Type();
  Type pointeeType;
  if (parser.parseType(pointeeType))
    return Type();
  if (parser.parseGreater())
    return Type();
  return get(parser.getContext(), pointeeType);
}

void PointerType::print(mlir::AsmPrinter &printer) const {
  printer << "<";
  printer.printType(getPointee());
  printer << '>';
}

Type BoolType::parse(mlir::AsmParser &parser) {
  return get(parser.getContext());
}

void BoolType::print(mlir::AsmPrinter &printer) const {}

//===----------------------------------------------------------------------===//
// StructType Definitions
//===----------------------------------------------------------------------===//

Type StructType::parse(mlir::AsmParser &parser) {
  llvm::SmallVector<mlir::Type> members;
  mlir::StringAttr id;
  bool body = false;
  bool packed = false;
  mlir::cir::ASTRecordDeclAttr ast = nullptr;

  if (parser.parseLess())
    return {};

  if (parser.parseAttribute(id))
    return {};

  if (parser.parseOptionalKeyword("packed").succeeded())
    packed = true;

  if (parser.parseOptionalKeyword("incomplete").failed()) {
    body = true;
    const auto delim = AsmParser::Delimiter::Braces;
    auto result = parser.parseCommaSeparatedList(delim, [&]() -> ParseResult {
      mlir::Type ty;
      if (parser.parseType(ty))
        return mlir::failure();
      members.push_back(ty);
      return mlir::success();
    });

    if (result.failed())
      return {};
  }

  parser.parseOptionalAttribute(ast);

  if (parser.parseGreater())
    return {};

  return StructType::get(parser.getContext(), members, id, body, packed,
                         std::nullopt);
}

void StructType::print(mlir::AsmPrinter &printer) const {
  printer << '<' << getTypeName() << " ";

  if (getPacked())
    printer << "packed ";

  if (!getBody()) {
    printer << "incomplete";
  } else {
    printer << "{";
    llvm::interleaveComma(getMembers(), printer);
    printer << "}";
  }

  if (getAst().has_value()) {
    printer << " ";
    printer.printAttribute(getAst().value());
  }

  printer << '>';
}

