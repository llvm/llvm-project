void Comma(void) {
  1, 2, 3;
}

void Assignment(int a) {
  a = 12;
  a += a;
}

void Conditionals(int a) {
  a ? 0 : 1;
  a ?: 0;
}

void BinaryOperators(int a, int b) {
  // Logical operators
  a || b;
  a && b;

  // Bitwise operators
  a | b;
  a ^ b;
  a & b;

  // Equality operators
  a == b;
  a != b;

  // Relational operators
  a < b;
  a > b;
  a <= b;
  a >= b;

  // Bit shifting operators
  a << b;
  a >> b;

  // Additive operators
  a + b;
  a - b;

  // Multiplicative operators
  a * b;
  a / b;
  a % b;
}

void UnaryOperators(int a, int *b) {
  // Cast operators
  (float)a;

  // ++, --, and ~ are covered elsewhere.

  -a;
  +a;
  &a;
  *b;
  !a;

  sizeof a;
  sizeof(int);
  _Alignof(int);
}

struct S {
  int a;
};

void PostfixOperators(int *a, struct S b, struct S *c) {
  a[0];
  UnaryOperators(*a, a);

  b.a;
  c->a;

  // Postfix ++ and -- are covered elsewhere.

  (int [4]){1, 2, 3, 4, };
  (struct S){1};
}

